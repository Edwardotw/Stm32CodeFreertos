#include "drv_dht11.h"
#include "bsp_usart.h"

//复位DHT11
static void DHT11_Rst(void)	   
{                 
	Dht11_Gpio_Init(OUT); 	//SET OUTPUT
    DHT11_DATA_OUT=0; 	//拉低DQ
    delay_xms(20);    	//拉低至少18ms
    DHT11_DATA_OUT=1; 	//DQ=1 
	delay_us(30);     	//主机拉高20~40us
}

//等待DHT11的回应
//返回1:未检测到DHT11的存在
//返回0:存在
static DHT11_STATUS DHT11_Check(void) 	   
{   
	uint8_t retry=0;
	Dht11_Gpio_Init(IN);//SET INPUT	 
    while (DHT11_DATA_IN&&retry<100)//DHT11会拉低40~80us
	{
		retry++;
		delay_us(1);
	};	 
	if(retry>=100)return STATUS_ERROR_SELFCHK;
	else retry=0;
    while (!DHT11_DATA_IN&&retry<100)//DHT11拉低后会再次拉高40~80us
	{
		retry++;
		delay_us(1);
	};
	if(retry>=100)return STATUS_ERROR_SELFCHK;	    
	return STATUS_OK;
}

//从DHT11读取一个位
//返回值：1/0
static uint8_t DHT11_Read_Bit(void) 			 
{
 	uint8_t retry=0;
	while(DHT11_DATA_IN&&retry<100)//等待变为低电平
	{
		retry++;
		delay_us(1);
	}
	retry=0;
	while(!DHT11_DATA_IN&&retry<100)//等待变高电平
	{
		retry++;
		delay_us(1);
	}
	delay_us(40);//等待40us
	if(DHT11_DATA_IN)return 1;
	else return 0;		   
}

//从DHT11读取一个字节
//返回值：读到的数据
static uint8_t DHT11_Read_Byte(void)    
{        
    uint8_t i,dat;
    dat=0;
	for (i=0;i<8;i++) 
	{
   		dat<<=1; 
	    dat|=DHT11_Read_Bit();
    }						    
    return dat;
}

//从DHT11读取一次数据
//temp:温度值(范围:0~50°)
//humi:湿度值(范围:20%~90%)
//返回值：0,正常;1,读取失败
DHT11_STATUS DHT11_Read_Data(float *temp,float *humi)    
{        
 	uint8_t buf[5];
	uint8_t i;
	DHT11_Rst();
	if(DHT11_Check()==0)
	{
		for(i=0;i<5;i++)//读取40位数据
		{
			buf[i]=DHT11_Read_Byte();
		}
		if((buf[0]+buf[1]+buf[2]+buf[3])==buf[4])
		{	
			*humi=(float)buf[0] + ((float)buf[1]) / 10;
			*temp=(float)buf[2] + ((float)buf[3]) / 10;
		}
	}else return STATUS_ERROR_READFAIL;
	return STATUS_OK;	    
}

//初始化DHT11的IO口 DQ 同时检测DHT11的存在
//返回1:不存在
//返回0:存在  
DHT11_STATUS Dht11_Init(void)
{
	Dht11_Gpio_Init(OUT);
	DHT11_Rst();
	return DHT11_Check();	
}

